c = a + b # インスタンスcにインスタンスaとbのデータを足し合わせた数値を入れる
d = a * b # インスタンスcにインスタンスaとbのデータを足し合わせた数値を入れる

print("hello world") # 「hello world」と言う文字を表示するよ
print("a=6とb=4を足し合わせた結果")
